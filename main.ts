// Declare variables
var num1: number = 56.9;
var num2: number = 89.7;
var num3: number = 90.2;
var average: number = (num1 + num2 + num3) / 3.0;

// Print the output
console.log("The average of " + String(num1) + ", " + String(num2) + " and " + String(num3) + " is " + String(average) + ".");